#include <boost/python.hpp>
#include <cstdio>

using namespace std;
namespace bp = boost::python;

//////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////

class Base {
public :
    int x;
    Base(int x) : x(x) {}
    Base(){}
};

class Test {
 
public:
    int N;
    Base b;
    Test(int N) : N(N) {
	b.x = 1;
    }
};

//////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////


BOOST_PYTHON_MODULE(py_test) {
    
    bp::class_<Base>("Base", bp::init<int>())
	.def_readwrite("x", &Base::x)
	;
    
    bp::class_<Test>("Test", bp::init<int>())
	.def_readwrite("N", &Test::N)
	.def_readwrite("b", &Test::b)
	//.def("redV2", &pyKS::PYredV2)
	;

}
